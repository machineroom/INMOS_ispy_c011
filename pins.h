/*
Definition of C011 <> RPI GPIO pins
pigpio lib uses BRCM pin numbering
*/


#include <pigpio.h>

#define D0 2      //BLACK
#define D1 3      //BROWN
#define D2 4      //RED
#define D3 17     //ORANGE
#define D4 18     //YELLOW
#define D5 27     //GREEN
#define D6 22     //BLUE
#define D7 23     //VIOLET

#define RS0 24    //GREY
#define RS1 10    //WHITE
#define RESET 9   //YELLOW
#define CS 25     //GREEN
#define RW 11     //BLUE
#define ANALYSE 8 //VIOLET

#define LOW 0
#define HIGH 1


